char	*ft_strupcase(char *str)
{
	int	x;

	x = 0;
	while (str[x] != '\0')
	{
		while ((str[x] >= 'a') && (str[x] <= 'z'))
		{
			str[x] -= 32;
		}
		x++;
	}
	return (str);
}
