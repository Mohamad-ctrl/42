int	ft_str_is_numeric(char *str)
{
	int	x;

	x = 0;
	while (str[x] != '\0')
	{
		if (!(str[x] <= '9' && str[x] >= '0'))
		{
			return (0);
		}
		x++;
	}
	return (1);
}