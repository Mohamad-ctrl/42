int	ft_atoi(char *str)
{
	int	x;
	int	s;

	x = 0;
	s = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		s *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		x = x * 10 + *str - '0';
		str++;
	}
	return (x * s);
}
