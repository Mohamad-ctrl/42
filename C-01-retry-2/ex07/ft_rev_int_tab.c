void	ft_rev_int_tab(int *tab, int size)
{
	int	x;
	int	y;

	y = 0;
	while (y < size / 2)
	{
		x = tab[y];
		tab[y] = tab[size - 1 - y];
		tab[size - 1 - y] = x;
		y++;
	}
}
